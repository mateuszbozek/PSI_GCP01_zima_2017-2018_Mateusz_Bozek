    public void uczenie() {

        boolean granica_bledu = false;  //zmienna, stwierdzajaca czy blad jest mozliwy do zaakceptowania
        int epoka=0; //liczba epok potrzebnych do nauki

        losowanie_wag(); // losowanie wag

        do{  //podczas jednej epoki nauka z ca≈Çego zestawu danych uczacych

            epoka++; // zwiekszenie numeru iteracji(epoki)
            blad_globalny=0.0; //zerowanie glownego bledu w celu sprawdzenia bledow podczas jednej iteracji
            for(int i=0;i<liczba_liter;i++) {
                output=funkcja_aktywacji(funkcja_sumujaca(tablica_liter_bit[i], tablica_wag));

                delta = tablica_oczekiwanych_wynikow[i] - output;   //obliczanie roznicy pomiedzy wynikiem oczekiwanym a wynikiem otrzymanym

                for(int j=0; j < liczba_wag;j++)
                    tablica_wag[j] +=wspolczynnik_nauki*delta*tablica_liter_bit[i][j]*(1-output)*output; // aktualizowanie wag

                blad_globalny+=delta*delta; //aktualizowanie bledu glownego
            }
            blad_globalny/=2;
            if(blad_globalny>0.001)
            {  granica_bledu = false; }
            else
            { granica_bledu = true; }


        }while(!granica_bledu);

        System.out.println("Blad globalny: " + blad_globalny);
        System.out.println("Liczba epok : " + epoka );

        for(int i=0;i<liczba_wag;i++)
            System.out.println("Waga dla : wejscie" + (i+1) + " = "+ tablica_wag[i]);
        System.out.println("\n");
    }
