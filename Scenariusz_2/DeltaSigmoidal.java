    public double funkcja_aktywacji(double sum){ //funkcja aktywacji
        return ( 1 / ( 1 + Math.exp(-1.0 * sum)));
    }

    //
    public double funkcja_sumujaca(int letter[], double[]weights) { // funkcja sumujaca
        double sum = 0.0;
        for(int i=0; i < liczba_wag; i++)
            sum += letter[i] * weights[i];
        return sum;
    }

